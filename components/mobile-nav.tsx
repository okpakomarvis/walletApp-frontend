"use client"

import Link from "next/link"
import { usePathname } from "next/navigation"
import { Home, Wallet, ArrowLeftRight, Activity, User } from "lucide-react"
import { cn } from "@/lib/utils"

const navItems = [
  { href: "/app/home", label: "Home", icon: Home },
  { href: "/app/wallets", label: "Wallets", icon: Wallet },
  { href: "/app/payments", label: "Payments", icon: ArrowLeftRight },
  { href: "/app/activity", label: "Activity", icon: Activity },
  { href: "/app/profile", label: "Profile", icon: User },
]

export function MobileNav() {
  const pathname = usePathname()

  return (
    <nav className="fixed bottom-0 left-0 right-0 z-50 border-t bg-card md:hidden">
      <div className="grid grid-cols-5 h-16">
        {navItems.map((item) => {
          const Icon = item.icon
          const isActive = pathname === item.href
          return (
            <Link
              key={item.href}
              href={item.href}
              className={cn(
                "flex flex-col items-center justify-center gap-1 transition-colors",
                isActive ? "text-primary" : "text-muted-foreground hover:text-foreground",
              )}
            >
              <Icon className={cn("h-5 w-5", isActive && "fill-primary/10")} />
              <span className="text-xs font-medium">{item.label}</span>
            </Link>
          )
        })}
      </div>
    </nav>
  )
}
